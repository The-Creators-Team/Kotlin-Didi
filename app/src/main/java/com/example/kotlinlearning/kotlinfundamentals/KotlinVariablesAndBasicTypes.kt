package com.example.kotlinlearning.kotlinfundamentals

fun main(args: Array<String>) {
    val range: Byte = 112
    println("$range")

    val temperature: Short = -11245
    println("$temperature")

    val score: Int = 100000
    println("$score")

    val highestScore: Long = 9999
    println("$highestScore")

    val distance = 999.5
    println("$distance")

    val distance1 = 19.5F
    println("$distance1")

    var test: Number = 12.2
    println("$test")

    test = 12
    println("$test")

    test = 120L
    println("$test")

    val letter: Char
    letter = 'k'
    println("$letter")

    val flag = true
    println("$flag")
}